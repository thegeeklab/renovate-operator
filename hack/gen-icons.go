package main

import (
	"errors"
	"fmt"
	"os"
	"path/filepath"
	"regexp"
	"strings"
)

type iconDef struct {
	fileName string
	variant  string
}

var icons = []iconDef{
	{fileName: "arrow-down-tray"},
	{fileName: "arrow-left"},
	{fileName: "arrow-path"},
	{fileName: "arrow-right-on-rectangle"},
	{fileName: "arrow-top-right-on-square"},
	{fileName: "bars-arrow-down"},
	{fileName: "bars-arrow-up"},
	{fileName: "check-circle"},
	{fileName: "chevron-right"},
	{fileName: "chevron-up-down"},
	{fileName: "document-text"},
	{fileName: "exclamation-triangle"},
	{fileName: "eye"},
	{fileName: "eye-slash"},
	{fileName: "funnel"},
	{fileName: "inbox"},
	{fileName: "information-circle"},
	{fileName: "key"},
	{fileName: "magnifying-glass"},
	{fileName: "x-circle"},
	{fileName: "x-mark"},
}

var (
	svgTagRe     = regexp.MustCompile(`<svg[^>]*>`)
	ariaHiddenRe = regexp.MustCompile(`\s*aria-hidden="true"`)
	dataSlotRe   = regexp.MustCompile(`\s*data-slot="icon"`)
	selfCloseRe  = regexp.MustCompile(`<(\w+)([^>]*?)/>`)

	errNoSVGTag = errors.New("no <svg> tag found")
)

func main() {
	if len(os.Args) < 2 { //nolint:mnd
		fmt.Fprintf(os.Stderr, "usage: %s <output-file>\n", os.Args[0])
		os.Exit(1)
	}

	outputPath := os.Args[1]

	nodeModules := filepath.Join("node_modules", "heroicons")
	if _, err := os.Stat(nodeModules); os.IsNotExist(err) {
		fmt.Fprintf(os.Stderr, "error: node_modules/heroicons not found. Run 'npm install' first.\n")
		os.Exit(1)
	}

	var b strings.Builder
	b.WriteString("// Code generated by hack/gen-icons.go; DO NOT EDIT.\n\n")
	b.WriteString("package view\n\n")

	for i, icon := range icons {
		variant := icon.variant
		if variant == "" {
			variant = "outline"
		}

		svgPath := filepath.Join(nodeModules, "24", variant, icon.fileName+".svg")

		raw, err := os.ReadFile(svgPath)
		if err != nil {
			fmt.Fprintf(os.Stderr, "error reading %s: %v\n", svgPath, err)
			os.Exit(1)
		}

		templName := "Icon" + kebabToPascal(icon.fileName)

		templ, err := svgToTempl(string(raw), templName)
		if err != nil {
			fmt.Fprintf(os.Stderr, "error converting %s: %v\n", icon.fileName, err)
			os.Exit(1)
		}

		b.WriteString(templ)

		if i < len(icons)-1 {
			b.WriteString("\n\n")
		} else {
			b.WriteString("\n")
		}
	}

	existing, _ := os.ReadFile(outputPath) //nolint:gosec
	if string(existing) == b.String() {
		fmt.Printf("no changes to %s\n", outputPath)

		return
	}

	if err := os.WriteFile(outputPath, []byte(b.String()), 0o600); err != nil { //nolint:gosec
		fmt.Fprintf(os.Stderr, "error writing %s: %v\n", outputPath, err)
		os.Exit(1)
	}

	fmt.Printf("generated %s with %d icons\n", outputPath, len(icons))
}

func kebabToPascal(s string) string {
	parts := strings.Split(s, "-")
	for i, part := range parts {
		if len(part) > 0 {
			parts[i] = strings.ToUpper(part[:1]) + part[1:]
		}
	}

	return strings.Join(parts, "")
}

func svgToTempl(raw, funcName string) (string, error) {
	svgMatch := svgTagRe.FindString(raw)
	if svgMatch == "" {
		return "", errNoSVGTag
	}

	inner := strings.TrimSpace(strings.TrimSuffix(strings.TrimSpace(raw[len(svgMatch):]), "</svg>"))

	svgTag := svgMatch
	svgTag = ariaHiddenRe.ReplaceAllString(svgTag, "")
	svgTag = dataSlotRe.ReplaceAllString(svgTag, "")
	svgTag = strings.TrimRight(svgTag, ">")
	svgTag += ` class={ class }>`

	inner = selfCloseRe.ReplaceAllString(inner, "<$1$2></$1>")

	innerLines := strings.Split(inner, "\n")

	var indented []string

	for _, line := range innerLines {
		trimmed := strings.TrimSpace(line)
		if trimmed != "" {
			indented = append(indented, "\t\t"+trimmed)
		}
	}

	var b strings.Builder
	fmt.Fprintf(&b, "templ %s(class string) {\n", funcName)
	fmt.Fprintf(&b, "\t%s\n", svgTag)

	for _, line := range indented {
		fmt.Fprintf(&b, "%s\n", line)
	}

	b.WriteString("\t</svg>\n}")

	return b.String(), nil
}
